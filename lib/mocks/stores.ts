export function getMockStores(
  page: number = 1,
  limit: number = 10
): APIResponse<PaginatedResponse<StoreResponse>> {
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;
  const paginatedData = mockStores.slice(startIndex, endIndex);

  return {
    statusCode: 200,
    data: {
      data: paginatedData,
      meta: {
        currentPage: page,
        totalPages: Math.ceil(mockStores.length / limit),
        totalItems: mockStores.length,
        itemsPerPage: limit,
        hasNextPage: endIndex < mockStores.length,
        hasPrevPage: page > 1,
      },
    },
  };
}
